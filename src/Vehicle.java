public class Vehicle {
    String id;
    String nama;
    String platKendaraan;
    boolean isRent;
    int kapasitas;

    public String getId() {
        return id;
    }

    public String getNama() {
        return nama;
    }

    public String getPlatKendaraan() {
        return platKendaraan;
    }

    public boolean getIsRent() {
        return isRent;
    }

    public int getKapasitas() {
        return kapasitas;
    }
}
